/*********************************************************************
*
*   PROCEDURE NAME:
*       toupper
*
*   DESCRIPTION:
*       Convert character to upper case.
*
*********************************************************************/
int toupper
    (
    int arg
    )
    {    
    /*------------------------------------------------------
    Check for lower case only
    ------------------------------------------------------*/
    if( arg < 'a' || arg > 'z' )
        return arg;
    
    arg -= ( 'a' - 'A' );
    
    return arg;
    
    } /* toupper() */
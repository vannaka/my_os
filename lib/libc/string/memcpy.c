/*********************************************************************
*
*   PROCEDURE NAME:
*       memcpy
*
*   DESCRIPTION:
*       Copy data from src to dest.
*
*	NOTE:
*		src and dst cannot overlap!
*
*********************************************************************/
void * memcpy
	(
	void * restrict 	dstptr, 
	const void * restrict 
						srcptr, 
	size_t size
	)
	{
	unsigned char * dst = (unsigned char *)dstptr;
	const unsigned char * src = (const unsigned char *)srcptr;
	
	for( size_t i = 0; i < size; i++ )
		{
		dst[ i ] = src[ i ];
		}
	
	return dstptr;
	
	} /* memcpy() */

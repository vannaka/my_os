/*********************************************************************
*
*   PROCEDURE NAME:
*       memset
*
*   DESCRIPTION:
*       Set a block of bytes to a given value.
*
*********************************************************************/
void * memset
	(
	void * 				bufptr, 	/* Address of block of memory	*/
	int 				value, 		/* Byte value to set each byte  */
	size_t 				size		/* Number of byte in block		*/
	)
	{
	unsigned char * buf = (unsigned char *)bufptr;
	
	for( size_t i = 0; i < size; i++ )
		{
		buf[ i ] = (unsigned char)value;
		}
	
	return bufptr;
	
	} /* memset() */

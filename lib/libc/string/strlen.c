/*********************************************************************
*
*   PROCEDURE NAME:
*       strlen
*
*   DESCRIPTION:
*       Get the lenght of a string.
*
*********************************************************************/
size_t strlen
	(
	const char * str
	) 
	{
	/*------------------------------------------------------
	Local Variables
	------------------------------------------------------*/
	size_t len = 0;
	
	/*------------------------------------------------------
	Look for null terminator.
	TODO: Do we need uper bound? Realistically, a zero
	probably exists fairly close to the real end of any
	given string.
	------------------------------------------------------*/
	while( str[ len ] )
		{
		len++;
		}
	
	return len;
	
	} /* strlen() */

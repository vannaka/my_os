/*********************************************************************
*
*   PROCEDURE NAME:
*       utoa
*
*   DESCRIPTION:
*       Convert signed int to string
*
*********************************************************************/
char * utoa
    (
    unsigned int        value,
    char *              result,
    int                 base
    )
    {
    // check that the base is valid
    if( base < 2 || base > 36 )
        {
        *result = '\0'; 
        return result;
        }

    char * ptr = result;
    char * ptr1 = result;
    char tmp_char;
    uint32_t tmp_value;

    do
        {
        tmp_value = value;
        value /= base;
        *ptr++ = alphabet[ 35 + ( tmp_value - value * base ) ];
        } 
        while( value );
    
    *ptr-- = '\0';
    while( ptr1 < ptr )
        {
        tmp_char = *ptr;
        *ptr-- = *ptr1;
        *ptr1++ = tmp_char;
        }
    
    return result;
    
    } /* utoa() */